public class Lab047 {
    public static void main(String[] args) {

        int a = 10;
        System.out.println(++a); // --> Pre change the value of a first
        // result --> ++a --> 1+a --> 1+10 -->11
        // value of a --> 11
        System.out.println(a);

        int a1 = 10;
        System.out.println(a1++); // this is post first assign then increase ++a--> 10
        System.out.println(a1);// Value of a --> a++--> a+1--> 10+1--> 11

        // int a = 10
        //System.out.println(++a);
        // Pre Increment
        // ++ in front of a Variable like a -> Pre
        // Pre -> Do increment first then print

        // Post Increment
        // ++ after the Variable like a -> Post
        // Post  -> First print then do the increment






    }
}
