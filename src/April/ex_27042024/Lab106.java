public class Lab106 {
    public static void main(String[] args) {

        // we can write a for loop with all numericals --> double, float, int, short, byte

        for (double i = 1.5; i < 12.30; i++) {
            System.out.println(i);
        }

        for (int j = -10; j < -5; j++) {
            System.out.println(j);
        }
        for (byte k = 0; k <3 ; k++) {
            System.out.println(k);
        }









    }
}
