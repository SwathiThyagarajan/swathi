package April.ex_27042024;

public class Lab116 {
    public static void main(String[] args) {

        for (int j = 1; j <=10 ; j++) {
            System.out.println(j);
        }

        // Reverse Loop

        for (int d = 10; d > 0 ; d--) {
            System.out.println(d);
        }

        // Instead of alphabets like i ,j we can use names also like Swathi. Modi etc...

        for (int swathi = 25; swathi <30 ; swathi++) {
            System.out.println(swathi);

        }




    }
}
